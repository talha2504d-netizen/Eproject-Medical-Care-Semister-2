    <h2>Common Symptoms of Cancer</h2>

    <ul>
        <li>Unexplained weight loss</li>
        <li>Persistent fatigue or weakness</li>
        <li>Lumps or swelling in the body</li>
        <li>Chronic cough or difficulty breathing</li>
        <li>Changes in skin appearance</li>
        <li>Unusual bleeding or pain</li>
    </ul>

    <h2>Importance of Early Detection</h2>

    <p>
        Detecting cancer at an early stage increases the chances of successful treatment.
        Regular medical checkups and screening tests help identify problems before they become severe.
    </p>

    <h2>Ways to Prevent Cancer</h2>

    <h3>Maintain a Healthy Lifestyle</h3>
    <p>
        Eating healthy foods, exercising regularly, and maintaining a healthy weight can reduce cancer risk.
    </p>

    <h3>Avoid Smoking and Tobacco</h3>
    <p>
        Smoking is one of the major causes of cancer. Avoiding tobacco products helps protect your lungs and overall health.
    </p>

    <h3>Regular Medical Checkups</h3>
    <p>
        Routine screenings and doctor consultations are important for early diagnosis and prevention.
    </p>

    <h2>Spread Awareness</h2>

    <p>
        Educating people about cancer symptoms, prevention, and treatment options can help communities fight this disease together.
        Awareness campaigns encourage early action and save lives.
    </p>

