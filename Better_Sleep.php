    <h2>Why Good Sleep Matters</h2>

    <p>Healthy sleep helps improve concentration, memory, mood, and immune system function. It also allows the body to repair and recharge properly.</p>

    <p>Lack of sleep can increase stress, reduce productivity, weaken immunity, and affect heart health and mental well-being.</p>

    <h2>Benefits of Better Sleep</h2>

    <h3>Improved Energy Levels</h3>
    <p>Quality sleep helps the body feel refreshed and energetic throughout the day.</p>

    <h3>Better Brain Function</h3>
    <p>Proper sleep improves focus, learning ability, memory, and decision-making skills.</p>

    <h3>Stronger Immune System</h3>
    <p>Good sleep supports the immune system and helps the body fight infections and illnesses.</p>

    <h3>Reduced Stress and Better Mood</h3>
    <p>Enough sleep helps control stress hormones and supports emotional balance and mental health.</p>

    <h2>Tips for Better Sleep</h2>

    <h3>Maintain a Sleep Schedule</h3>
    <p>Try to sleep and wake up at the same time every day to improve your body clock.</p>

    <h3>Avoid Screen Time Before Bed</h3>
    <p>Reducing mobile phone and TV usage before sleeping can help the brain relax faster.</p>

    <h3>Create a Comfortable Sleep Environment</h3>
    <p>Keep your bedroom quiet, dark, and cool for better sleep quality.</p>

    <h3>Limit Caffeine at Night</h3>
    <p>Avoid tea, coffee, and energy drinks before bedtime as they can disturb sleep.</p>

    <h3>Practice Relaxation Techniques</h3>
    <p>Deep breathing, meditation, and light stretching before bed can help the mind and body relax.</p>

