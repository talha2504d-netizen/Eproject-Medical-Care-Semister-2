    <h2>What is Diabetes?</h2>
    <p>Diabetes is a condition in which the body either does not produce enough insulin or cannot effectively use the insulin it produces.</p>

    <h2>Types of Diabetes</h2>

    <h3>Type 1 Diabetes</h3>
    <p>The body does not produce insulin. It usually appears in children or young adults.</p>

    <h3>Type 2 Diabetes</h3>
    <p>The body becomes resistant to insulin or doesn’t produce enough. It is the most common type.</p>

    <h3>Gestational Diabetes</h3>
    <p>This type occurs during pregnancy and usually disappears after delivery, but increases risk of Type 2 diabetes later.</p>

    <h2>Common Symptoms</h2>
    <p>• Frequent urination<br>
       • Excessive thirst<br>
       • Fatigue<br>
       • Blurred vision<br>
       • Slow healing wounds</p>

    <h2>Management Tips</h2>
    <p>Maintaining a healthy diet, regular exercise, monitoring blood sugar, and taking prescribed medication can help control diabetes effectively.</p>

