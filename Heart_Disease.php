    <h2>Causes of Heart Disease</h2>
    <p>Heart disease can develop due to multiple factors including high blood pressure, unhealthy diet, obesity, diabetes, and smoking.</p>

    <h2>Common Symptoms</h2>

    <h3>Chest Pain</h3>
    <p>One of the most common symptoms is chest discomfort or pain, especially during physical activity.</p>

    <h3>Shortness of Breath</h3>
    <p>Difficulty in breathing may occur due to reduced blood flow and heart efficiency.</p>

    <h3>Fatigue</h3>
    <p>Feeling tired even after minimal activity can be an early warning sign.</p>

    <h2>Prevention Tips</h2>
    <p>Maintaining a healthy lifestyle can significantly reduce the risk of heart disease.</p>

    <p>Regular exercise, a balanced diet, avoiding smoking, and managing stress are key preventive measures.</p>

