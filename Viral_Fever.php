    <h2>Common Symptoms of Viral Fever</h2>
    <h3>High Fever</h3>
    <p>Sudden rise in body temperature is the most common sign of viral infection.</p>

    <h3>Body Pain and Weakness</h3>
    <p>Patients often feel muscle pain, joint pain, and overall tiredness.</p>

    <h3>Headache and Chills</h3>
    <p>Severe headache and cold shivering are also common symptoms.</p>

