    <h2>Common Symptoms of COVID-19</h2>

    <ul>
        <li>Fever or chills</li>
        <li>Dry cough</li>
        <li>Shortness of breath</li>
        <li>Loss of taste or smell</li>
        <li>Body aches and fatigue</li>
        <li>Sore throat and headache</li>
    </ul>

    <h2>How COVID-19 Spreads</h2>

    <p>
        The virus mainly spreads through respiratory droplets when an infected person coughs,
        sneezes, or talks. It can also spread by touching contaminated surfaces.
    </p>

    <h2>Prevention Tips</h2>

    <h3>Wash Hands Regularly</h3>
    <p>
        Clean your hands frequently with soap and water or use an alcohol-based sanitizer.
    </p>

    <h3>Wear a Mask</h3>
    <p>
        Wearing a face mask in crowded places helps reduce the spread of infection.
    </p>

    <h3>Maintain Social Distance</h3>
    <p>
        Keep a safe distance from people who are sick or showing symptoms.
    </p>

    <h3>Get Vaccinated</h3>
    <p>
        Vaccination helps protect against severe illness and reduces complications caused by COVID-19.
    </p>

    <h2>When to Seek Medical Help</h2>

    <p>
        Seek immediate medical attention if you experience difficulty breathing,
        chest pain, confusion, or persistent high fever.
    </p>

